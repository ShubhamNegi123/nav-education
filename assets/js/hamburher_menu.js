function hamburger_menu(){
    $('.nav-bar-tabs').animate({
        right: '0px'
    });
    $('.nav-close-button').animate({
        right: '170px'
    });
}   
function close_Hamburger_menu(){
    $('.nav-bar-tabs').animate({
        right: '-200px'
    });
    $('.nav-close-button').animate({
        right: '-25px'
    });
} 